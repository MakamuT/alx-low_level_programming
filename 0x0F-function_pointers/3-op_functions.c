#include "3-calc.h"
/**
 * op_add - adds
 * @a: int
 * @b: int
 * Return: 0
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts
 * @a: int
 * @b: int
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiples
 * @a: int
 * @b: int
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function divides
 * @a: int
 * @b: int
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function finds the remained of a division
 * @a: int
 * @b: int
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
